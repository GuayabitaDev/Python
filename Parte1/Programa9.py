#Programa para aprender WHILE

Answer = None

while Answer != "A" and Answer != "B" and Answer != "C":
    Answer = input("Que opcion prefieres? [A, B o C]")

if Answer == "A":
    print("Elegiste A")

elif Answer == "B":
    print("Elegiste B")

elif Answer == "C":
    print("Elegiste C")

else:
    print("Respuesta Invalida")

